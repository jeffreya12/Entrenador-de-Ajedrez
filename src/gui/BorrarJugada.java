    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        int respuesta = JOptionPane.showConfirmDialog(null,
                "Â¿Seguro que desea borrar esta jugada?", "Atencion",
                JOptionPane.OK_CANCEL_OPTION);
        if (respuesta == JOptionPane.YES_OPTION){
            Jugada jugadaTemp = (Jugada) jugadasComboBox.getSelectedItem();
            try{
                jugadaTemp.getImagen().delete();
            }
            catch (Exception e){}
            partida.getListaDeJugadas().remove(jugadasComboBox.getSelectedItem());
            application.EntrenadorDeAjedrez.setPartida(partida);
            application.EntrenadorDeAjedrez.write();
            this.dispose();
        }
    }//GEN-LAST:event_aceptarActionPerformed

    

